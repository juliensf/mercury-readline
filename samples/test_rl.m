:- module test_rl.
:- interface.

:- import_module io.

:- pred main(io::di, io::uo) is det.

%---------------------------------------------------------------------------%
%---------------------------------------------------------------------------%

:- implementation.

:- import_module readline.

:- import_module list.
:- import_module string.

%---------------------------------------------------------------------------%

main(!IO) :-
    readline(">> ", Line, !IO),
    io.format("You entered: \"%s\"\n", [s(Line)], !IO),
    main(!IO).

%---------------------------------------------------------------------------%
:- end_module test_rl.
